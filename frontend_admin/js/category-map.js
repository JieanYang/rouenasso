function categoryIdToName(id) {
    if(id == 1) {
        return '类型1';
    } else if(id == 1) {
        return '类型1';
    } else if(id == 2) {
        return '类型1';
    } else if(id == 3) {
        return '类型1';
    } else if(id == 4) {
        return '类型1';
    } else if(id == 99) {
        return '首页公告';
    } else {
        return '类型不存在';
    }
}